public class DataTypesDemo {
    public static void main(String[] args) {
        // Integer data types
        byte age = 30;
        short year = 2025;
        int population = 1000000;
        long nationalDebt = 1000000000000L; // 'L' for long literal

        // Floating-point data types
        float temperature = 98.6f; // 'f' for float literal
        double pi = 3.1415926535;

        // Character data type
        char grade = 'A';

        // Boolean data type
        boolean isJavaFun = true;

        // String (not a primitive, but fundamental)
        String name = "Java Programmer";

        System.out.println("Age: " + age);
        System.out.println("Year: " + year);
        System.out.println("Population: " + population);
        System.out.println("National Debt: " + nationalDebt);
        System.out.println("Temperature: " + temperature);
        System.out.println("Pi: " + pi);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java Fun? " + isJavaFun);
        System.out.println("Name: " + name);
    }
}